    public void loadTableViewLibrarians(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/librarymanagement","root","");
            PreparedStatement ps = conn.prepareStatement("SELECT * FROM librarians ");
            ResultSet rs = ps.executeQuery();
            
            ArrayList<Librarian> alist = new ArrayList<Librarian>(); 
            
            while(rs.next()){
                Librarian l =new Librarian(rs.getInt(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getInt(6));
                alist.add(l);
            }
            DefaultTableModel model = (DefaultTableModel) tableLibrarians.getModel();
            Object[] row = new Object[6];
            for(int i=0;i<alist.size();i++){
                row[0]=alist.get(i).id;
                row[1]=alist.get(i).name;
                row[2]=alist.get(i).password;
                row[3]=alist.get(i).city;
                row[4]=alist.get(i).email;
                row[5]=alist.get(i).contact;
                
                model.addRow(row);
            }
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e,"Library Management System", JOptionPane.ERROR_MESSAGE);
        }
    }
