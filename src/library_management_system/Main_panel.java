    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
        int bID,sID,issued;
        bID =Integer.parseInt(TxtReturnBookID.getText());
        sID =Integer.parseInt(txtReturnStudentID.getText());
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/librarymanagement","root","");
            con.setAutoCommit(false);
            PreparedStatement ps = con.prepareStatement("DELETE FROM issuedbooks WHERE bookID=? AND studentID=?");
            ps.setInt(1, bID);
            ps.setInt(2, sID);
            ps.executeUpdate();
            
            PreparedStatement ps2 = con.prepareStatement("SELECT issued FROM books WHERE ID=?");
            ps2.setInt(1, bID);
            ResultSet rs= ps2.executeQuery();
            while(rs.next()){
                issued = rs.getInt(1) - 1 ;
                PreparedStatement ps1 = con.prepareStatement("UPDATE books SET issued=? WHERE ID=?");
                ps1.setInt(1, issued );
                ps1.setInt(2, bID);
                ps1.executeUpdate();
            }
            con.setAutoCommit(true);
            JOptionPane.showMessageDialog(null,"Book Returned Successfully.","Library Management System", JOptionPane.PLAIN_MESSAGE);
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(null,e,"Library Management System", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnReturnActionPerformed

